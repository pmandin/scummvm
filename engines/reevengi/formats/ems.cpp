void EmsArchive::scanArchive(void) {
	uint8 block[0x800];
	uint32 length, vv, tmp;

	/* Scan archive for TIM files */
	for (uint32 startOffset=0; startOffset<_stream->size() - 0x800; startOffset+=0x800) {
		uint32 myStart = startOffset;
		length = 0;

		_stream->seek(startOffset);
		_stream->read(block, 0x800);

		tmp = block[0]|(block[1]<<8)|(block[2]<<16)|(block[3]<<24);
		if (tmp==0x10) {
			tmp = block[4]|(block[5]<<8)|(block[6]<<16)|(block[7]<<24);
			if ((tmp==8) || (tmp==9) || (tmp==2) || (tmp==3)) {
				/* TIM image */
				length = 8;
				if (block[4]>=8) {
					vv = block[8]|(block[9]<<8)|(block[10]<<16)|(block[11]<<24);
					length += vv;
				}

				if (length>=0x800) {
					startOffset += 0x800;
					_stream->read(block, 0x800);

					vv = length-0x800;
				} else {
					vv = length;
				}
				length += block[vv]|(block[vv+1]<<8)|(block[vv+2]<<16)|(block[vv+3]<<24);

				Common::SeekableSubReadStream *subStream = new Common::SeekableSubReadStream(_stream,
					myStart, myStart + length);

				debug(3, "\t{\"%s\", 0x00, 1, %d, 0}, /* 0x%08x */", computeStreamMD5AsString(*subStream).c_str(), length, myStart);

				if ((length & 0x7ff)!=0) {
					length &= -0x800;
				} else {
					length -= 0x800;
				}
				startOffset = myStart + length;
			}
		} else if (tmp>=0x100) {
			/* EMD file */
			length = tmp;

			tmp = block[4]|(block[5]<<8)|(block[6]<<16)|(block[7]<<24);
			length += tmp * 4;

			if ((length>=2048) && (length<300<<10)) {
				Common::SeekableSubReadStream *subStream = new Common::SeekableSubReadStream(_stream,
					myStart, myStart + length);

				debug(3, "\t{\"%s\", 0x00, 0, %d, 0}, /* 0x%08x */", computeStreamMD5AsString(*subStream).c_str(), length, myStart);

				if ((length & 0x7ff)!=0) {
					length &= -0x800;
				} else {
					length -= 0x800;
				}
				startOffset = myStart + length;
			}
		}
	}
}

Common::SeekableReadStream *EmsArchive::createReadStreamForMember(int numModel, int fileType) const {
	if (!_stream)
		return nullptr;

	debug(3, "re2:ems: Searching for em0%02x.%s...", numModel, (fileType==0 ? "emd" : "tim"));

	for (uint32 i=0; i<sizeof(emsItems)/sizeof(ems_item_t); i++) {
		if ((emsItems[i].length==0) || (emsItems[i].fType!=fileType) || (emsItems[i].model!=numModel))
			continue;

		/* Already found ? */
		if (emsItems[i].offset != 0xffffffffUL) {
			Common::SeekableSubReadStream *subStream = new Common::SeekableSubReadStream(_stream,
				emsItems[i].offset, emsItems[i].offset + emsItems[i].length);

			return subStream;
		}

		for (uint32 startOffset=0; startOffset<_stream->size() - emsItems[i].length; startOffset+=0x800) {
			Common::SeekableSubReadStream *subStream = new Common::SeekableSubReadStream(_stream,
				startOffset, startOffset + emsItems[i].length);

			if (computeStreamMD5AsString(*subStream).equalsIgnoreCase(emsItems[i].md5sum)) {
				debug(3, "re2:ems:  Found at offset 0x%04x", startOffset);

				emsItems[i].offset = startOffset;
				return subStream;
			}

			delete subStream;
		}
	}

	debug(3, "re2:ems: Not found");
	return nullptr;
}

